// --- Random Generation Logic ---
const getRandomItem = <T,>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)];

export const generateRandomCharacterDescription = (): string => {
    const randomSettings: Record<string, string> = {};
    Object.entries(NCL_OPTIONS).forEach(([key, value]) => {
        // Higher chance to include core attributes like gender and lifepath
        if (key === 'gender' || key === 'lifePath' || Math.random() < 0.6) {
             const options = value.options.filter(opt => opt !== value.options[0] && opt !== value.label); // Exclude placeholders
             if (options.length > 0) {
                 randomSettings[key] = getRandomItem(options);
             }
        }
    });
    
    let parts: string[] = [];
    const gender = randomSettings.gender || 'person';
    const lifePath = randomSettings.lifePath ? `a ${randomSettings.lifePath}` : '';
    parts.push(`A cyberpunk ${gender}${lifePath ? `, formerly ${lifePath}` : ''}.`);

    if (randomSettings.expression) parts.push(`Their expression is ${randomSettings.expression}.`);
    
    let hairParts: string[] = [];
    if (randomSettings.hairStyle) hairParts.push(randomSettings.hairStyle);
    if (randomSettings.hairColor) hairParts.push(randomSettings.hairColor);
    if (hairParts.length > 0) parts.push(`They have ${hairParts.join(' ')} hair.`);

    let clothingParts: string[] = [];
    if (randomSettings.headwear) clothingParts.push(`wearing ${randomSettings.headwear}`);
    if (randomSettings.outerwear) clothingParts.push(`a ${randomSettings.outerwear}`);
    if (randomSettings.innerwear) clothingParts.push(`over a ${randomSettings.innerwear}`);
    if (randomSettings.legwear) clothingParts.push(`and ${randomSettings.legwear}`);
    if (randomSettings.footwear) clothingParts.push(`with ${randomSettings.footwear}`);
    if (clothingParts.length > 0) parts.push(`Dressed in ${clothingParts.join(', ')}.`);
    
    let cyberwareParts: string[] = [];
    if (randomSettings.faceCyberware) cyberwareParts.push(`facial cyberware including ${randomSettings.faceCyberware}`);
    if (randomSettings.bodyCyberware) cyberwareParts.push(`body modifications like ${randomSettings.bodyCyberware}`);
    if (cyberwareParts.length > 0) parts.push(`Chrome includes ${cyberwareParts.join(' and ')}.`);

    return parts.join(' ').replace(/ ,/g, ',');
};

export const generateRandomNCLFullPrompt = (): string => {
    // 1. 角色設定 (Character Setup)
    const character = generateRandomCharacterDescription();

    // 2. 武器庫&載具 (Armory & Vehicles)
    const allWeapons = Object.values(NIGHT_CITY_WEAPONS).flat();
    const weapon = getRandomItem(allWeapons);
    const allVehicles = Object.values(NIGHT_CITY_VEHICLES).flat();
    const vehicle = getRandomItem(allVehicles);

    // 3. 任務夥伴 (Mission Partners)
    const allCompanions = Object.values(NIGHT_CITY_COMPANIONS).flat();
    const companion = getRandomItem(allCompanions);

    // 4. 幻夢設定 (Dream/Mission Setup)
    const allMissions = NIGHT_CITY_MISSIONS.flatMap(m => m.options);
    const mission = allMissions.length > 0 
        ? getRandomItem(allMissions) 
        : "a secret operation against a megacorp"; // Fallback

    // 5. 任務場景 (Mission Scene)
    const allScenes = Object.values(NIGHT_CITY_LEGENDS)
        .flatMap(v => Object.values(v as any))
        .flat()
        .map(s => (s as { name: string; prompt: string }).prompt);
    const scenePrompt = getRandomItem(allScenes);
    
    // Construct the final prompt
    const prompt = `
**1. 任務場景:**
${scenePrompt}

**2. 角色設定:**
${character}

**3. 任務夥伴:**
Teamed up with ${companion}.

**4. 武器庫 & 載具:**
Armed with a ${weapon} and navigating the streets in a ${vehicle}.

**5. 任務設定:**
The objective is to: ${mission}.
`.trim();

    return prompt;
};